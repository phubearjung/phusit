    private void bn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bn2ActionPerformed
        if(tb.isRowSelected(selected)){
            double moneySelected= Double.valueOf(tb.getValueAt(selected,3).toString().replace(",",""));
            System.out.println(moneySelected);
            total-=moneySelected;
            lbtotal.setText(""+df.format(total)+" Baht ");
            if(total==0){
                lbtotal.setText("0.00 Baht ");
            }
            model.removeRow(selected);
        }else{
            JOptionPane.showMessageDialog(this,"กรุณาเลือกรายการที่ท่านต้องการยกเลิก","คำเตือน",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_bn2ActionPerformed

