    private void bn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bn1ActionPerformed
        Vector v=new Vector();
        if(t2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"กรุณาใส่จำนวน","คำเตือน",JOptionPane.ERROR_MESSAGE);
        }if(checkMenu()){
            int a=Integer.parseInt(model.getValueAt(n,2).toString())+Integer.parseInt(t2.getText()); 
            model.setValueAt(a,n,2);
            double price=a*Double.valueOf(tb.getValueAt(n,1).toString());
            model.setValueAt(df.format(price),n,3);
            money=Double.valueOf(t1.getText())*Double.valueOf(t2.getText());
            total+=money;
            lbtotal.setText(""+df.format(total)+" Baht ");
        }else{       
            v.add(cbFood.getSelectedItem().toString()); 
            v.add(t1.getText());
            v.add(t2.getText());
            money=Double.valueOf(t1.getText())*Double.valueOf(t2.getText());
            v.add(df.format(money)); 
            total+=money;
            model.addRow(v);
            lbtotal.setText(""+df.format(total)+" Baht ");
    }//GEN-LAST:event_bn1ActionPerformed
}
    private void tbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbMouseClicked
        selected = tb.getSelectedRow();
        String name=tb.getValueAt(selected,0).toString();
        System.out.println("ท่านต้องการยกเลิกเมนูที่ "+selected);
        t2.setText(tb.getValueAt(selected,2).toString());
        for(int i=0;i<cbFood.getItemCount();i++){
            if(name.equals(cbFood.getItemAt(i))){
                cbFood.setSelectedIndex(i);
            }
        }
    }//GEN-LAST:event_tbMouseClicked

    private void bn3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bn3ActionPerformed
        double before=Double.valueOf(tb.getValueAt(selected,1).toString())*Double.valueOf(tb.getValueAt(selected,2).toString());
        
        tb.setValueAt(t2.getText(),selected,2);
        int amount=Integer.valueOf(t2.getText());
        double bnew=amount*Double.valueOf(tb.getValueAt(selected, 1).toString());
        tb.setValueAt(df.format(bnew),selected,3);
        total=total-before+bnew;
        lbtotal.setText(""+df.format(total)+" Baht ");
    }//GEN-LAST:event_bn3ActionPerformed

